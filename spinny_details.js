const BASE_URL = "https://api.spinny.com/v3/api/pdp/price-breakdown/";

function fetchCarPriceData(carId) {
  const url = `${BASE_URL}${carId}/v2/`;
  console.log(`üì° Fetching price data for car ID: ${carId}...`);

  return new Promise((resolve, reject) => {
    https.get(url, res => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => {
        try {
          const json = JSON.parse(data);
          console.log(`‚úÖ Fetched data for car ID: ${carId}`);
          resolve({ id: carId, data: json });
        } catch (err) {
          console.error(`‚ùå Error parsing response for ID ${carId}: ${err}`);
          resolve({ id: carId, error: 'Invalid JSON' });
        }
      });
    }).on('error', err => {
      console.error(`‚ùå HTTP error for car ID ${carId}: ${err.message}`);
      resolve({ id: carId, error: err.message });
    });
  });
}

